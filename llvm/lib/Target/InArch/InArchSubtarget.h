#ifndef LLVM_LIB_TARGET_INARCH_INARCHSUBTARGET_H
#define LLVM_LIB_TARGET_INARCH_INARCHSUBTARGET_H

#include "InArchFrameLowering.h"
#include "InArchISelLowering.h"
#include "InArchInstrInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "InArchGenSubtargetInfo.inc"

namespace llvm {

class StringRef;
class TargetMachine;

class InArchSubtarget : public InArchGenSubtargetInfo {
  virtual void anchor();

  InArchRegisterInfo RegInfo;
  InArchInstrInfo InstrInfo;
  InArchFrameLowering FrameLowering;
  InArchTargetLowering TLInfo;
  SelectionDAGTargetInfo TSInfo;

public:
  InArchSubtarget(const Triple &TT, const std::string &CPU, const std::string &FS,
              const TargetMachine &TM);

  /// Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);

  const InArchInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const InArchFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const InArchTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  const InArchRegisterInfo *getRegisterInfo() const override { return &RegInfo; }
  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }
};

} // end namespace llvm

#endif // LLVM_LIB_TARGET_INARCH_INARCHSUBTARGET_H